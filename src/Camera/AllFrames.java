    private void jLabel49MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel49MouseClicked
        File imageFile = null;
        face = null ;
        jLabel40.setIcon(null);
        BufferedImage bufferedImage = null ;
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new java.io.File("."));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "jpg", "gif", "png");
        chooser.addChoosableFileFilter(filter);
        chooser.setDialogTitle("Browse Image with Face");
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int result = chooser.showSaveDialog(null);
        if(result == JFileChooser.APPROVE_OPTION){
        imageFile = chooser.getSelectedFile();
        String imagePath = imageFile.getAbsolutePath();
        Mat matimg = Imgcodecs.imread(imagePath);
        bufferedImage = matToBufferedImage(matimg);
        facedetector.detectMultiScale(matimg, facedetections);
        for(Rect rect : facedetections.toArray()){
            face = bufferedImage.getSubimage(rect.x, rect.y, rect.width, rect.height);
        }
        if(face !=null){
        jLabel40.setIcon(new ImageIcon(face));
                SelectImage.hide();
        StudentDetails.show();
        }
        else{
            JOptionPane.showMessageDialog(null, "No face Detected !",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }    
        }
        
    }//GEN-LAST:event_jLabel49MouseClicked

