    private void jLabel47MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel47MouseClicked
        if(!"".equals(jTextField13.getText()) || !"Username".equals(jTextField13.getText())){
        String password1,password2;
        password1 = String.valueOf(jPasswordField3.getText());
        password2 = String.valueOf(jPasswordField4.getText());
        if(password1.equals(password2)){
            try {
                if(obj.teacherIDExists(jTextField6.getText())){
                    JOptionPane.showMessageDialog(new JFrame(), "Username already exists !!",
                            "Dialog", JOptionPane.ERROR_MESSAGE);
                }
                else{
                obj.teacherDB(jTextField6.getText(), password1);
                JOptionPane.showMessageDialog(null, "Succssfully Registered !");
                jTextField6.setText("Username");
                jPasswordField3.setText("12345678");
                jPasswordField4.setText("abcdefgh");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(new JFrame(), ex, "Dialog",
                        JOptionPane.ERROR_MESSAGE);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AllFrames.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            JOptionPane.showMessageDialog(new JFrame(), "Passwords do not match !!",
                    "Dialog", JOptionPane.ERROR_MESSAGE);
            jPasswordField3.setText("12345678");
            jPasswordField4.setText("abcdefgh");
            jTextField6.setText("Username");
            
        }
        }
        else{
            JOptionPane.showMessageDialog(null, "Username is Incorrect !");
        }
    }//GEN-LAST:event_jLabel47MouseClicked

